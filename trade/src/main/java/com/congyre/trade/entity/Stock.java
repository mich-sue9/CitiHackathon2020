package com.congyre.trade.entity;

public class Stock {

    private String ticker;
    private int amount;

    public String getTicker() {
        return ticker;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public void setTicker(String ticker) {
        this.ticker = ticker;
    }




    
}
